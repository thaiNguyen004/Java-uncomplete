public class example {

    public static void main(String[] args) {
        /*
        Cú pháp generics được thể hiện ở những ví dụ trước có thể tổng quát hóa. 
        Dưới đây là cú pháp để khai báo một lớp generic:
        
            class class-name<type-param-list> {
                // ...
            }
        
        Và đây là cú pháp đầy đủ để khai báo một tham chiếu đến một lớp generic và tạo một phiên bản:
            class-name<type-arg-list> var-name = new class-name<type-arg-list>(cons-arg-list);
        Trong đó type-param-list là một danh sách các tham số kiểu, cách nhau bởi dấu phẩy, 
        và type-arg-list là danh sách các đối số kiểu tương ứng với type-param-list. 
        Các đối số kiểu này được truyền vào khi tạo một phiên bản của lớp generic.
        
        cons-arg-list là danh sách các tham số truyền vào cho constructor (hàm tạo) của lớp. 
        Nó là một danh sách các giá trị được truyền vào cho hàm tạo của lớp generic khi khởi tạo một đối tượng của lớp đó.
        */
    }
    
}
